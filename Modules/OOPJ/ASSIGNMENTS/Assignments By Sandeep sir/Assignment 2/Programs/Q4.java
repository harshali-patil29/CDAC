public class Q4 {
    public static void main(String[] args) {
        //l
        int number = 100;
        
        // Convert int to long
        long longValue = (long) number;
        System.out.println("Int to long: " + longValue);
        
        // Convert int to float
        float floatValue = (float) number;
        System.out.println("Int to float: " + floatValue);
        
        // Convert long to int
        long anotherLong = 1000L;
        int intValueFromLong = (int) anotherLong;
        System.out.println("Long to int: " + intValueFromLong);
    }
}



